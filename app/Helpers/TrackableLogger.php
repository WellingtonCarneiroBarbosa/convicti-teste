<?php

namespace App\Helpers;

use Log;

class TrackableLogger
{
    protected array $context;

    /**
     * Creates a logger instance with debug backtrace
     */
    public function __construct()
    {
        $this->context = debug_backtrace();
    }

    /**
     * Log an emergency message to the logs.
     *
     * @param  string  $message
     * @param  array  $context
     * @return void
     */
    public function emergency($message, array $context = [])
    {
        return Log::emergency($message, array_merge($this->context, $context));
    }

    /**
     * Log an alert message to the logs.
     *
     * @param  string  $message
     * @param  array  $context
     * @return void
     */
    public function alert($message, array $context = [])
    {
        return Log::alert($message, array_merge($this->context, $context));
    }

    /**
     * Log a critical message to the logs.
     *
     * @param  string  $message
     * @param  array  $context
     * @return void
     */
    public function critical($message, array $context = [])
    {
        return Log::critical($message, array_merge($this->context, $context));
    }

    /**
     * Log an error message to the logs.
     *
     * @param  string  $message
     * @param  array  $context
     * @return void
     */
    public function error($message, array $context = [])
    {
        return Log::error($message, array_merge($this->context, $context));
    }

    /**
     * Log a warning message to the logs.
     *
     * @param  string  $message
     * @param  array  $context
     * @return void
     */
    public function warning($message, array $context = [])
    {
        return Log::warning($message, array_merge($this->context, $context));
    }

    /**
     * Log a notice to the logs.
     *
     * @param  string  $message
     * @param  array  $context
     * @return void
     */
    public function notice($message, array $context = [])
    {
        return Log::notice($message, array_merge($this->context, $context));
    }

    /**
     * Log an informational message to the logs.
     *
     * @param  string  $message
     * @param  array  $context
     * @return void
     */
    public function info($message, array $context = [])
    {
        return Log::info($message, array_merge($this->context, $context));
    }

    /**
     * Log a debug message to the logs.
     *
     * @param  string  $message
     * @param  array  $context
     * @return void
     */
    public function debug($message, array $context = [])
    {
        return Log::debug($message, array_merge($this->context, $context));
    }
}
